def meters_to_feet(meters):
    return meters * 3.28084

def meters_to_yards(meters):
    return meters * 1.09361

def celsius_to_fahrenheit(celsius):
    return celsius * 9/5 + 32

def square_meter_to_acre(square_meter):
    return square_meter * 0.000247105

def kilogram_to_ounce(kilogram):
    return kilogram * 35.27396

def kilogram_to_pound(kilogram):
    return kilogram * 2.20462

def pascal_to_bar(pascal):
    return pascal / 100000

def pascal_to_psi(pascal):
    return pascal * 0.00014503773773375

def liter_to_cubic_meter(liter):
    return liter / 1000

def joule_to_calorie(joule):
    return joule * 0.000239006

def joule_to_watt(joule):
    return joule / 1

def meter_per_second_to_feet_per_second(mps):
    return mps * 3.28084

def meter_per_second_to_miles_per_hour(mps):
    return mps * 2.23694

